def game_loop(args):
    pygame.init()
    pygame.font.init()
    world = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(2.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        display.fill((0,0,0))
        pygame.display.flip()

        hud = HUD(args.width, args.height)
        world = World(client.get_world(), hud, args)
        map = client.get_world().get_map()
        player = world.player
        other_player = world.other_player
        walker = world.walker

        player_control    = carla.VehicleControl()
        other_player_control = carla.VehicleControl()
        walker_control = carla.WalkerControl()
        player_lane = world.player_lane

        other_player_control.throttle = random.uniform(0.7, 0.8)
        player_control.throttle = min(other_player_control.throttle - random.uniform(0.2, 0.3),0.5)
        if player_lane == 'right':
            walker_control.direction = carla.Vector3D(x = 1.0, y = 0.0, z = 0.0)
        else:
            walker_control.direction = carla.Vector3D(x = -1.0, y = 0.0, z = 0.0)
        walker_control.speed = 0.5

        if player_lane == 'left':
            player_goal = carla.Location(x = 82.53, y = -65.04, z = 7.57)
        else:
            player_goal = carla.Location(x = 86.13, y = -65.04, z = 7.57)
        goal_flag = False

        player_loc = player.get_location()
        other_loc = other_player.get_location()
        player_vel = player.get_velocity()
        other_vel = other_player.get_velocity()

        # For recording results:
        player_x, player_y, player_z = [], [], []
        player_v_x, player_v_y, player_v_z = [], [], []
        other_x, other_y, other_z = [], [], []
        other_v_x, other_v_y, other_v_z = [], [], []
        time_history = []
        time_counter = 0.0
        player_x.append(player_loc.x)
        player_y.append(player_loc.y)
        player_z.append(player_loc.z)
        player_v_x.append(player_vel.x * 3.6)
        player_v_y.append(player_vel.y * 3.6)
        player_v_z.append(player_vel.z * 3.6)
        other_x.append(other_loc.x)
        other_y.append(other_loc.y)
        other_z.append(other_loc.z)
        other_v_x.append(other_vel.x * 3.6)
        other_v_y.append(other_vel.y * 3.6)
        other_v_z.append(other_vel.z * 3.6)
        time_history.append(0.001*time_counter)



        clock = pygame.time.Clock()
        while not goal_flag:
            clock.tick_busy_loop(60)
            time_counter += clock.get_rawtime()

            if walker is not None:
                walker_loc = walker.get_location()

                other_to_walker_vector = [other_loc.x - walker_loc.x, other_loc.y - walker_loc.y, other_loc.z - walker_loc.z]
                other_to_walker_distance = np.linalg.norm(other_to_walker_vector)

                if other_to_walker_distance <= 9.0:
                    other_player_control.throttle = 0.0
                    other_player_control.brake = 0.6

            player.apply_control(player_control)
            other_player.apply_control(other_player_control)
            if walker is not None:
                walker.apply_control(walker_control)

            player_to_goal_vector = [player_loc.x - player_goal.x, player_loc.y - player_goal.y, player_loc.z - player_goal.z]
            player_to_goal_distance = np.linalg.norm(player_to_goal_vector)
            if player_to_goal_distance <= 6.0:
                goal_flag = True

            player_loc = player.get_location()
            other_loc = other_player.get_location()
            player_vel = player.get_velocity()
            other_vel = other_player.get_velocity()

            player_x.append(player_loc.x)
            player_y.append(player_loc.y)
            player_z.append(player_loc.z)
            player_v_x.append(player_vel.x * 3.6)
            player_v_y.append(player_vel.y * 3.6)
            player_v_z.append(player_vel.z * 3.6)
            other_x.append(other_loc.x)
            other_y.append(other_loc.y)
            other_z.append(other_loc.z)
            other_v_x.append(other_vel.x * 3.6)
            other_v_y.append(other_vel.y * 3.6)
            other_v_z.append(other_vel.z * 3.6)
            time_history.append(0.001*time_counter)



            world.tick(clock)
            world.render(display)
            pygame.display.flip()

    finally:

        if (world and world.recording_enabled):
            client.stop_recorder()

        if world is not None:
            world.destroy()

        example_dict = {'player_x': player_x, 'player_y': player_y, 'player_z': player_z,
                        'player_v_x': player_v_x, 'player_v_y': player_v_y, 'player_v_z': player_v_z,
                        'other_x': other_x, 'other_y': other_y, 'other_z': other_z,
                        'other_v_x': other_v_x, 'other_v_y': other_v_y, 'other_v_z': other_v_z,
                        'time_history': time_history}
        pickle_out = open("data_set/neg_label{}.pickle".format(args.count),"wb")
        pickle.dump(example_dict, pickle_out)
        pickle_out.close()

        pygame.quit()


